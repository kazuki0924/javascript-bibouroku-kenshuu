/**
 * * 数値の足し算には「+」、引き算には「-」の記号を用います。
 * クォーテーションで囲われた中身は文字列と解釈されます。
 * * かけ算は「*」、割り算は「/」を用います。
 * * 余りの計算は「%」を用います。
 */

// 5と3を足した値を出力してください
console.log(5 + 3);

// 20から8を引いた値を出力してください
console.log(20 - 8);

// "4 + 5" を文字列として出力してください
console.log('4 + 5');

// 8と4をかけた結果を出力してください
console.log(8 * 4);

// 24を4で割った結果を出力してください
console.log(24 / 4);

// 7を2で割った余りを出力してください
console.log(7 % 2);
