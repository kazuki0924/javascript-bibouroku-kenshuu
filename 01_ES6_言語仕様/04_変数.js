/**
 * * 「変数」は値の箱です。
 * * 箱についている名前が「変数名」です。
 * 「=」を用いて右辺を左辺に代入します。
 * * 「let」はこれから変数を定義するという宣言で、その後ろに変数名を書き、値を代入します。
 * 変数を使うと変数の部分は代入した値に置き換わります。
 * 変数を用いることで同じ値を繰り返し使えるようになり、変更へも対応しやすくなります。
 *
 * 変数の命名ルール:
 * なるべく英単語を用いる
 * キャメルケースを使う
 * 数字開始を避ける
 */

// 変数nameを定義し、「にんじゃわんこ」を代入してください
let name = 'にんじゃわんこ';

// 変数nameの値を出力してください
console.log(name);

// 変数lengthを定義してください
let length = 5;

// 変数lengthの値を出力してください
console.log(length);

// 変数lengthを用いて、円の面積を出力してください
console.log(length * length * 3);
